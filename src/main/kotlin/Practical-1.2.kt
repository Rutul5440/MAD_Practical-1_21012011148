fun main(){
    var a:Int = 10
    println("Integer value: $a")
    var b = a.toDouble()
    println("Double value from integer: $b")

    var c:String = "10"
    println("String value: $c")
    var d = c.toInt()
    println("Integer value from string value: $d")

    var e = c.toDouble()
    println("Double value from string value: $e")

}